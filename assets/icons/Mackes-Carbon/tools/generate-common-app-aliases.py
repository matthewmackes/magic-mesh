def validate_alias(name: str) -> None:
    if name.endswith(".svg"):
        raise ValueError(f"Alias should not include .svg: {name}")
    if "/" in name or "\\" in name or not name:
        raise ValueError(f"Invalid alias name: {name!r}")


def write_icon(path: Path, data: bytes) -> bool:
    if path.exists():
        if path.read_bytes() != data:
            raise RuntimeError(f"{path} already exists with different content")
        return False
    path.write_bytes(data)
    return True


def main() -> None:
    created = 0
    unchanged = 0
    seen = set()

    for source, aliases in ALIAS_GROUPS:
        source_path = SCALABLE / source
        if not source_path.exists():
            raise FileNotFoundError(source_path)

        data = source_path.read_bytes()
        for alias in aliases:
            validate_alias(alias)
            if alias in seen:
                raise ValueError(f"Duplicate alias: {alias}")
            seen.add(alias)

            for suffix in ("", "-symbolic"):
                target = APP_DIR / f"{alias}{suffix}.svg"
                if write_icon(target, data):
                    created += 1
                else:
                    unchanged += 1

    print(f"aliases: {len(seen)}")
    print(f"created: {created}")
    print(f"unchanged: {unchanged}")


if __name__ == "__main__":
    main()
