/// Default endpoint cert/key — generated by `found` (ONBOARD-4) and
/// persisted alongside the Nebula files. Distinct from the covert
/// tunnel's `lighthouse.crt` so the two TLS identities don't collide.
pub const DEFAULT_CERT_PATH: &str = "/etc/nebula/enroll-endpoint.crt";
/// Default endpoint key path paired with [`DEFAULT_CERT_PATH`].
pub const DEFAULT_KEY_PATH: &str = "/etc/nebula/enroll-endpoint.key";

/// Conventional first-host overlay IP the lighthouse advertises.
/// Matches the csr-watcher + CLI defaults.
const LIGHTHOUSE_OVERLAY_IP: &str = "10.42.0.1";

/// Whole-connection budget — TLS handshake + request read + response
/// write. Bounds slowloris without hurting a real one-shot enroll.
const CONNECTION_TIMEOUT: Duration = Duration::from_secs(15);

/// Accept-loop back-off when a single accept fails — keeps the
/// listener from spinning on a hostile TCP/4243 flood.
const ACCEPT_BACKOFF: Duration = Duration::from_millis(500);

/// Per-connection plumbing the spawned task needs. Cheap to clone
/// (Arc + small owned strings/paths).
#[derive(Clone)]
struct EnrollContext {
    backend: Arc<dyn NebulaCertBackend>,
    db_path: PathBuf,
    workgroup_root: PathBuf,
    paths: SignCsrPaths,
    local_node_id: String,
    external_addr: String,
    /// This lighthouse's own overlay IP, used only for the self-fallback entry
    /// when its directory row hasn't replicated yet (founding LH). Resolved from
    /// the live `nebula1` at construction, falling back to the conventional
    /// first-host IP; overridable in tests for determinism.
    self_overlay: String,
}

impl EnrollContext {
    /// LIGHTHOUSE-10 — build the bundle's lighthouse roster from the replicated
    /// peer directory so a joining node learns the FULL set of lighthouses, not
    /// just the one it enrolled through (that single-entry roster was the bug
    /// that made multiple lighthouses non-redundant). Reads every
    /// `role=="lighthouse"` directory record carrying an overlay + external
    /// address. Always includes THIS lighthouse from its own config — the
    /// founding lighthouse (or any LH before its heartbeat replicates) must hand
    /// out at least itself, and self may not be in the directory yet.
    fn roster(&self) -> Vec<LighthouseEntry> {
        let peers = mackes_mesh_types::peers::read_peers(&mackes_mesh_types::peers::peers_dir(
            &self.workgroup_root,
        ));
        let mut entries: Vec<LighthouseEntry> =
            mackes_mesh_types::lighthouse::roster_from_directory(&peers)
                .into_iter()
                .map(|a| LighthouseEntry {
                    node_id: a.node_id,
                    overlay_ip: a.overlay_ip,
                    external_addr: a.external_addr,
                })
                .collect();
        // Guarantee self is present — deduped by external_addr so we don't
        // double-list when this LH's own directory row is already included.
        if !entries
            .iter()
            .any(|e| e.external_addr == self.external_addr)
        {
            entries.push(LighthouseEntry {
                node_id: self.local_node_id.clone(),
                overlay_ip: self.self_overlay.clone(),
                external_addr: self.external_addr.clone(),
            });
        }
        entries
    }
}

/// The `/enroll` listener worker.
pub struct NebulaEnrollListener {
    bind_addr: SocketAddr,
    cert_path: PathBuf,
    key_path: PathBuf,
    ctx: EnrollContext,
}

impl NebulaEnrollListener {
    /// Construct with the lighthouse's signing config. `external_addr`
    /// is the lighthouse's public-internet `ip:port` (what the signed
    /// bundle advertises so the peer dials the public addr — closes
    /// the MESH-2 class of static_host_map bug).
    #[must_use]
    pub fn new(
        backend: Arc<dyn NebulaCertBackend>,
        db_path: PathBuf,
        workgroup_root: PathBuf,
        paths: SignCsrPaths,
        local_node_id: String,
        external_addr: String,
    ) -> Self {
        let bind_addr = SocketAddr::from(([0, 0, 0, 0], DEFAULT_ENROLL_PORT));
        Self {
            bind_addr,
            cert_path: PathBuf::from(DEFAULT_CERT_PATH),
            key_path: PathBuf::from(DEFAULT_KEY_PATH),
            ctx: EnrollContext {
                backend,
                db_path,
                workgroup_root,
                paths,
                local_node_id,
                external_addr,
                self_overlay: crate::voip_rtt::own_nebula_ip()
                    .unwrap_or_else(|| LIGHTHOUSE_OVERLAY_IP.to_string()),
            },
        }
    }

    /// Override this lighthouse's self-fallback overlay IP (tests — keeps the
    /// roster deterministic regardless of the host's live `nebula1`).
    #[must_use]
    pub fn with_self_overlay(mut self, overlay: impl Into<String>) -> Self {
        self.ctx.self_overlay = overlay.into();
        self
    }

    /// Override the bind address (tests bind `127.0.0.1:0`).
    #[must_use]
    pub fn with_bind_addr(mut self, addr: SocketAddr) -> Self {
        self.bind_addr = addr;
        self
    }

    /// Override the endpoint cert path.
    #[must_use]
    pub fn with_cert(mut self, path: PathBuf) -> Self {
        self.cert_path = path;
        self
    }

    /// Override the endpoint key path.
    #[must_use]
    pub fn with_key(mut self, path: PathBuf) -> Self {
        self.key_path = path;
        self
    }
}

#[async_trait::async_trait]
impl Worker for NebulaEnrollListener {
    fn name(&self) -> &'static str {
        "nebula-enroll-listener"
    }

    async fn run(&mut self, mut shutdown: ShutdownToken) -> anyhow::Result<()> {
        let acceptor = match build_acceptor(&self.cert_path, &self.key_path) {
            Ok(a) => a,
            Err(e) => {
                // No endpoint cert (peer-role box) or unreadable key:
                // warn + exit, supervisor backs off. Same posture as
                // the NF-1.5 covert listener.
                tracing::warn!(
                    cert = %self.cert_path.display(),
                    error = %e,
                    "nebula-enroll-listener: cert load failed; worker exiting",
                );
                return Err(anyhow::anyhow!("cert load: {e}"));
            }
        };
        let listener = match TcpListener::bind(self.bind_addr).await {
            Ok(l) => l,
            Err(e) => {
                tracing::warn!(
                    bind = %self.bind_addr,
                    error = %e,
                    "nebula-enroll-listener: bind failed; worker exiting",
                );
                return Err(anyhow::anyhow!("bind: {e}"));
            }
        };
        tracing::info!(
            bind = %self.bind_addr,
            "nebula-enroll-listener: accepting peer enrollments",
        );

        loop {
            tokio::select! {
                _ = shutdown.wait() => {
                    tracing::info!("nebula-enroll-listener: shutdown requested");
                    return Ok(());
                }
                accept = listener.accept() => {
                    match accept {
                        Ok((tcp, peer)) => {
                            let acceptor = acceptor.clone();
                            let ctx = self.ctx.clone();
                            tokio::spawn(async move {
                                let outcome = tokio::time::timeout(
                                    CONNECTION_TIMEOUT,
                                    serve_connection(acceptor, tcp, ctx),
                                )
                                .await;
                                match outcome {
                                    Ok(Ok(())) => {}
                                    Ok(Err(e)) => tracing::debug!(
                                        %peer, error = %e,
                                        "nebula-enroll-listener: connection error",
                                    ),
                                    Err(_) => tracing::debug!(
                                        %peer,
                                        "nebula-enroll-listener: connection timed out",
                                    ),
                                }
                            });
                        }
                        Err(e) => {
                            tracing::debug!(
                                error = %e,
                                "nebula-enroll-listener: accept failed; backing off",
                            );
                            tokio::time::sleep(ACCEPT_BACKOFF).await;
                        }
                    }
                }
            }
        }
    }
}

/// Build a TLS 1.2/1.3 acceptor from the endpoint PEM cert + key.
/// Pure once the files are read; returns a `String` error so the
/// worker can warn-and-exit uniformly.
fn build_acceptor(
    cert_path: &std::path::Path,
    key_path: &std::path::Path,
) -> Result<TlsAcceptor, String> {
    let cert_pem =
        std::fs::read(cert_path).map_err(|e| format!("read {}: {e}", cert_path.display()))?;
    let key_pem =
        std::fs::read(key_path).map_err(|e| format!("read {}: {e}", key_path.display()))?;
    let cert_chain: Vec<CertificateDer<'static>> = CertificateDer::pem_slice_iter(&cert_pem)
        .collect::<Result<Vec<_>, _>>()
        .map_err(|e| format!("parse cert pem: {e}"))?;
    if cert_chain.is_empty() {
        return Err(format!("no certs in {}", cert_path.display()));
    }
    let key: PrivateKeyDer<'static> =
        PrivateKeyDer::from_pem_slice(&key_pem).map_err(|e| format!("parse key pem: {e}"))?;
    let provider = Arc::new(rustls::crypto::ring::default_provider());
    let config = rustls::ServerConfig::builder_with_provider(provider)
        .with_safe_default_protocol_versions()
        .map_err(|e| format!("protocol versions: {e}"))?
        .with_no_client_auth()
        .with_single_cert(cert_chain, key)
        .map_err(|e| format!("server config: {e}"))?;
    Ok(TlsAcceptor::from(Arc::new(config)))
}

/// Handle exactly one TLS connection: handshake, read one HTTP
/// request, dispatch, write the response, close.
async fn serve_connection(
    acceptor: TlsAcceptor,
    tcp: tokio::net::TcpStream,
    ctx: EnrollContext,
) -> anyhow::Result<()> {
    let mut tls = acceptor.accept(tcp).await?;
    // Read until parse_request yields a verdict. Cap the buffer at the
    // body cap + header slack so a peer can't exhaust memory.
    let max_buf = MAX_ENROLL_BODY + 8 * 1024;
    let mut buf: Vec<u8> = Vec::with_capacity(4096);
    let mut chunk = [0_u8; 4096];
    let response = loop {
        match parse_request(&buf) {
            ParseOutcome::Complete(req) => break dispatch(req, &ctx).await,
            ParseOutcome::Invalid(why) => {
                break HttpResponse {
                    status: 400,
                    body: error_json(why),
                }
            }
            ParseOutcome::NeedMore => {}
        }
        if buf.len() >= max_buf {
            break HttpResponse {
                status: 400,
                body: error_json("request too large"),
            };
        }
        let n = tls.read(&mut chunk).await?;
        if n == 0 {
            // EOF before a complete request.
            break HttpResponse {
                status: 400,
                body: error_json("incomplete request"),
            };
        }
        buf.extend_from_slice(&chunk[..n]);
    };
    tls.write_all(&response.to_wire()).await?;
    tls.flush().await?;
    let _ = tls.shutdown().await;
    Ok(())
}

/// Route a parsed request. Only `POST /enroll` is real; everything
/// else gets a terse status. The signing call shells out to
/// nebula-cert + touches SQLite, so it runs on the blocking pool.
async fn dispatch(
    req: crate::nebula_enroll_endpoint::HttpRequest,
    ctx: &EnrollContext,
) -> HttpResponse {
    if req.path != "/enroll" {
        return HttpResponse {
            status: 404,
            body: error_json("not found"),
        };
    }
    if req.method != "POST" {
        return HttpResponse {
            status: 405,
            body: error_json("method not allowed"),
        };
    }
    let ctx = ctx.clone();
    let body = req.body;
    let join = tokio::task::spawn_blocking(move || {
        handle_enroll(
            &*ctx.backend,
            &ctx.db_path,
            &ctx.workgroup_root,
            &ctx.paths,
            ctx.roster(),
            &body,
        )
    })
    .await;
    match join {
        Ok(resp) => resp,
        Err(e) => {
            tracing::warn!(error = %e, "nebula-enroll-listener: signing task panicked");
            HttpResponse {
                status: 500,
                body: error_json("internal signing error"),
            }
        }
    }
}

fn error_json(message: &str) -> Vec<u8> {
    serde_json::json!({ "error": message })
        .to_string()
        .into_bytes()
}

