//! ONBOARD-2 — the lighthouse `/enroll` rustls HTTPS listener.
//!
//! Binds a dedicated TLS listener (default `:4243`) on a node that is
//! `am_lighthouse`, accepts one HTTP request per connection, and
//! routes `POST /enroll` to the pure handler in
//! [`crate::nebula_enroll_endpoint`]. This is the network bootstrap
//! that fixes MESH-1: a NAT'd / remote peer POSTs its CSR here and
//! gets back a signed nebula bundle over the public internet, authed
//! by the single-use join-token bearer — no QNM-Shared pre-mount.
//!
//! Trust model: the endpoint serves a **self-signed** identity whose
//! SHA-256 fingerprint is pinned into every minted join token
//! (ONBOARD-1). The peer verifies that fingerprint before sending its
//! CSR (ONBOARD-3), so there's no trust-on-first-use MITM window and
//! no CA-chain dependency at enroll time.
//!
//! Runtime reachability mirrors the NF-1.5 covert listener: a
//! peer-role box has no endpoint cert, so `build_acceptor` fails, the
//! worker logs at warn + exits, and the supervisor's OnFailure policy
//! backs off. On a lighthouse with the cert present it serves enrolls.
//!
//! §1: Nebula-native control plane — no Tailscale / Headscale / DERP.
//! §3: rustls + ring provider, TLS 1.2/1.3.

