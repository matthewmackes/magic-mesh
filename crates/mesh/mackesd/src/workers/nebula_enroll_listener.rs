    #[tokio::test]
    async fn end_to_end_tls_enroll_returns_bundle() {
        use crate::enrollment::build_identity;
        use crate::nebula_enroll::{build_pending, parse_join_token};
        use rustls::pki_types::ServerName;
        use rustls::RootCertStore;

        let tmp = tempfile::tempdir().unwrap();
        let root = tmp.path();
        let ctx = roster_ctx(root);

        // Generate the endpoint identity + a pinning client root store.
        let id = crate::nebula_enroll_endpoint::generate_endpoint_identity(&["localhost".into()])
            .expect("gen");
        let crt = root.join("e.crt");
        let key = root.join("e.key");
        std::fs::write(&crt, &id.cert_pem).unwrap();
        std::fs::write(&key, &id.key_pem).unwrap();
        let acceptor = build_acceptor(&crt, &key).expect("acceptor");

        // Seed an issued bearer + build the CSR body the peer would POST.
        let token = parse_join_token("mesh:test-mesh@10.0.0.5:4243#e2e-bearer").unwrap();
        crate::bearer_ledger::record_issued(root, &token.bearer).unwrap();
        let pending = build_pending(&build_identity(), "peer:anvil", "anvil", token);
        let body = serde_json::to_vec(&pending).unwrap();

        // Bind + serve exactly one connection through the real path.
        let listener = TcpListener::bind("127.0.0.1:0").await.unwrap();
        let addr = listener.local_addr().unwrap();
        let server = tokio::spawn(async move {
            let (tcp, _) = listener.accept().await.unwrap();
            serve_connection(acceptor, tcp, ctx).await
        });

        // Pinning client: trust ONLY the generated cert.
        let mut roots = RootCertStore::empty();
        for cert in CertificateDer::pem_slice_iter(id.cert_pem.as_bytes()) {
            roots.add(cert.unwrap()).unwrap();
        }
        let provider = Arc::new(rustls::crypto::ring::default_provider());
        let client_config = rustls::ClientConfig::builder_with_provider(provider)
            .with_safe_default_protocol_versions()
            .unwrap()
            .with_root_certificates(roots)
            .with_no_client_auth();
        let connector = tokio_rustls::TlsConnector::from(Arc::new(client_config));
        let tcp = tokio::net::TcpStream::connect(addr).await.unwrap();
        let server_name = ServerName::try_from("localhost").unwrap();
        let mut tls = connector
            .connect(server_name, tcp)
            .await
            .expect("tls handshake");

        let request = format!(
            "POST /enroll HTTP/1.1\r\nHost: localhost\r\nContent-Length: {}\r\nConnection: close\r\n\r\n",
            body.len()
        );
        tls.write_all(request.as_bytes()).await.unwrap();
        tls.write_all(&body).await.unwrap();
        tls.flush().await.unwrap();

        let mut resp = Vec::new();
        tls.read_to_end(&mut resp).await.unwrap();
        server.await.unwrap().expect("server ok");

        let text = String::from_utf8_lossy(&resp);
        assert!(
            text.starts_with("HTTP/1.1 200 OK"),
            "got: {}",
            &text[..text.len().min(60)]
        );
        let body_start = text.find("\r\n\r\n").unwrap() + 4;
        let bundle: crate::ca::bundle::NebulaBundle =
            serde_json::from_str(&text[body_start..]).expect("bundle json");
        assert_eq!(bundle.mesh_id, "test-mesh");
        assert_eq!(bundle.lighthouses[0].external_addr, "203.0.113.7:4242");
        // Single-use: the bearer is spent after the network enroll.
        assert!(!crate::bearer_ledger::is_pending(
            root,
            &pending.token.bearer
        ));
    }

